package basic;

public class RelationalOperators {

    public static void main(String[] args) {
        int x = 1, y=5;
        System.out.println("x<y "+ (x<y));
        System.out.println("x>y "+ (x>y));
        System.out.println("x<=y "+ (x<=y));
        System.out.println("x>=y "+ (x>=y));
        System.out.println("x==y "+ (x==y));
        System.out.println("x!=y "+ (x!=y));
        System.out.println("x<=1 "+ (x<=1));
        System.out.println("x<1 "+ (x<1));


    }
}
